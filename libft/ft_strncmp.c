#include "ft_head.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i] || (i + 1) == n)
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
